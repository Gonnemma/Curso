let contador = 1
for (contador >= 100; contador++;){
    if (contador % 2 == 0)
        console.log(`El numero ${contador} es divisible por 2.`)
    if (contador % 3 == 0)
        console.log(`El numero ${contador} es divisible por 3.`)
else 
    console.log ("el numero no es divisible por 2 o 3.")
}


